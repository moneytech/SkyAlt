void UiLogo_convertZlib(void)
{
	Image4 bmp;
	if (Image4_initBmp(&bmp, "logo.bmp"))
	{
		Image1 img = Image4_convertToImage1(&bmp);

		int plainN = Image1_num(&img);
		int max_compressN = OsZlib_maxCompressSize(plainN);

		UCHAR* data = Os_malloc(max_compressN);
		int compressN = OsZlib_compress(img.data, data, plainN, max_compressN, OsZlib_BEST_COMPRESS);
		printf("Compress size: %d\n", compressN);

		int line = 0;
		int i;
		for (i = 0; i < compressN; i++)
		{
			printf("%d,", data[i]);
			if (line++ == 20)
				printf("\n"), line = 0;
		}

		Os_free(data, max_compressN);
		Image4_free(&bmp);
		Image1_free(&img);
	}
}

